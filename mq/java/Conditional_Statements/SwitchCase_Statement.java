package mq.java.Conditional_Statements;

public class SwitchCase_Statement 
{
	
	
		public static void main(String[] args) 
		{
			
			
			int month=3;
			
			switch (month) 
			{
			case 1:
				System.out.println("Jan");
				break;
				
			case 2:
				System.out.println("feb");
				break;
				
			case 3:
				System.out.println("mar");
				break;
				
			case 4:
				System.out.println("apr");
				break;

			default:
				System.out.println("Month Mismatch");
				break;
			}
			
			
			System.out.println("Run Continued");
			
		}
}
